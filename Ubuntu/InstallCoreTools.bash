# Install Core Tools

## Firewall & Configure
sudo apt-get -y install ufw && sudo ufw allow ssh && sudo ufw enable -y

## System Monitor
sudo apt-get -y install glances

## HardwareInfo
sudo apt-get -y install dmidecode

## Disk Management Tools
sudo apt-get -y install parted

# Network Management Tools
sudo apt-get -y install iputils-ping
sudo apt-get -y install net-tools  

## Disk Performance Tools
sudo apt-get -y install atop
sudo apt-get -y install htop
sudo apt-get -y install iotop
sudo apt-get -y install bashtop

# Install File Editor
sudo apt-get -y install nano

# storage
sudo apt-get -y install duf

## 7zip
sudo apt-get -y install p7zip-full
